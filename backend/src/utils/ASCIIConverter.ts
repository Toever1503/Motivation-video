/**
 * Bo dau 1 ky tu
 *
 * @param ch
 * @return
 */
function removeAccent(ch: string): string {
    let index: number = SOURCE_CHARACTERS.indexOf(ch);
    if (index >= 0) {
        ch = DESTINATION_CHARACTERS[index];
    }
    return ch;
}

/**
 * Bo dau 1 chuoi
 *
 * @param s
 * @return
 */
function removeAccents(s: string): string {
    let sb: string = '';
    for (let i = 0; i < s.length; i++) {
        sb += removeAccent(s[i]);
    }
    return sb;
}

export default async function utf8ToAscii(s: String | string) {
    return removeAccents(s.trim().replaceAll("\\s{2,}", " ")).replaceAll(" ", "-").toLowerCase();
}